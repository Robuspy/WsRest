package com.pablosrl.data;

public class ChequesPendientes {
    private String codCliente;
    private String fecEmision;
    private String fecVencimiento;
    private String diasVencimiento;
    private String tipoDocumento;
    private String nroValor;
    private String estadoValor;
    private String montoValor;



    public ChequesPendientes(){}

	public String getCodCliente() {
		return codCliente;
	}

	public void setCodCliente(String codCliente) {
		this.codCliente = codCliente;
	}

	public String getFecEmision() {
		return fecEmision;
	}

	public void setFecEmision(String fecEmision) {
		this.fecEmision = fecEmision;
	}

	public String getFecVencimiento() {
		return fecVencimiento;
	}

	public void setFecVencimiento(String fecVencimiento) {
		this.fecVencimiento = fecVencimiento;
	}

	public String getDiasVencimiento() {
		return diasVencimiento;
	}

	public void setDiasVencimiento(String diasVencimiento) {
		this.diasVencimiento = diasVencimiento;
	}

	public String getTipoDocumento() {
		return tipoDocumento;
	}

	public void setTipoDocumento(String tipoDocumento) {
		this.tipoDocumento = tipoDocumento;
	}

	public String getNroValor() {
		return nroValor;
	}

	public void setNroValor(String nroValor) {
		this.nroValor = nroValor;
	}

	public String getEstadoValor() {
		return estadoValor;
	}

	public void setEstadoValor(String estadoValor) {
		this.estadoValor = estadoValor;
	}

	public String getMontoValor() {
		return montoValor;
	}

	public void setMontoValor(String montoValor) {
		this.montoValor = montoValor;
	}





}
