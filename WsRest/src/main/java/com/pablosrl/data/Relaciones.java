package com.pablosrl.data;

public class Relaciones {
	private String codRelacion;
	private String codUm;
	private String mult;
	private String div;
	private String porDefecto;
	private String descUm;
	private String codEmpresa;

	public Relaciones() {
		super();
	}
	public String getCodRelacion() {
		return codRelacion;
	}
	public void setCodRelacion(String codRelacion) {
		this.codRelacion = codRelacion;
	}
	public String getCodUm() {
		return codUm;
	}
	public void setCodUm(String codUm) {
		this.codUm = codUm;
	}
	public String getMult() {
		return mult;
	}
	public void setMult(String mult) {
		this.mult = mult;
	}
	public String getDiv() {
		return div;
	}
	public void setDiv(String div) {
		this.div = div;
	}
	public String getPorDefecto() {
		return porDefecto;
	}
	public void setPorDefecto(String porDefecto) {
		this.porDefecto = porDefecto;
	}
	public String getDescUm() {
		return descUm;
	}
	public void setDescUm(String descUm) {
		this.descUm = descUm;
	}
	public String getCodEmpresa() {
		return codEmpresa;
	}
	public void setCodEmpresa(String codEmpresa) {
		this.codEmpresa = codEmpresa;
	}


}
