package com.pablosrl.data.cuentas_cobrar;

public class ClienteSaldo {

	private String codCliente;
    private String nombreCliente;
    private String saldoGs;
    private String limiteCredito;
    private String posibleCompra;
    private String chequesPendientes;
    private String saldoFavor;
    private String notasCredito;
    private String saldoTotal;
    private String mensaje;
    
    
	public String getCodCliente() {
		return codCliente;
	}
	public void setCodCliente(String codCliente) {
		this.codCliente = codCliente;
	}
	public String getNombreCliente() {
		return nombreCliente;
	}
	public void setNombreCliente(String nombreCliente) {
		this.nombreCliente = nombreCliente;
	}
	public String getSaldoGs() {
		return saldoGs;
	}
	public void setSaldoGs(String saldoGs) {
		this.saldoGs = saldoGs;
	}
	public String getLimiteCredito() {
		return limiteCredito;
	}
	public void setLimiteCredito(String limiteCredito) {
		this.limiteCredito = limiteCredito;
	}
	public String getPosibleCompra() {
		return posibleCompra;
	}
	public void setPosibleCompra(String posibleCompra) {
		this.posibleCompra = posibleCompra;
	}
	public String getChequesPendientes() {
		return chequesPendientes;
	}
	public void setChequesPendientes(String chequesPendientes) {
		this.chequesPendientes = chequesPendientes;
	}
	public String getSaldoFavor() {
		return saldoFavor;
	}
	public void setSaldoFavor(String saldoFavor) {
		this.saldoFavor = saldoFavor;
	}
	public String getNotasCredito() {
		return notasCredito;
	}
	public void setNotasCredito(String notasCredito) {
		this.notasCredito = notasCredito;
	}
	public String getSaldoTotal() {
		return saldoTotal;
	}
	public void setSaldoTotal(String saldoTotal) {
		this.saldoTotal = saldoTotal;
	}
	public String getMensaje() {
		return mensaje;
	}
	public void setMensaje(String mensaje) {
		this.mensaje = mensaje;
	}
    
    
    

}
